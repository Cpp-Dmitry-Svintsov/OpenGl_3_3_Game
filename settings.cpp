int AsSettings::Get_Random(int start, int range)
{
	return rand() % range + start;
}

//=================================================================================================================
float AsSettings::Distance_Beetween_Two_Points(glm::vec3 pos_a, glm::vec3 pos_b)
{
	float dx = pos_b.x - pos_a.x;
	float dz = pos_b.z - pos_a.z;
	return sqrt(pow(dx, 2) + pow(dz, 2));
}

