#include "vbo_bones.h"

//=================================================================================================
// constructors
//=================================================================================================
AVbo_Bones::AVbo_Bones(std::vector<SVertex_Bones>& vertices)
{
	glGenBuffers(1, &Id);
	glBindBuffer(GL_ARRAY_BUFFER, Id);
	glBufferData(GL_ARRAY_BUFFER, vertices.size() * sizeof(SVertex_Bones), vertices.data(), GL_STATIC_DRAW);
}

//=================================================================================================
// public section:
//=================================================================================================
void AVbo_Bones::Bind()
{
	glBindBuffer(GL_ARRAY_BUFFER, Id);
}

//=================================================================================================
void AVbo_Bones::Unbind()
{
	glBindBuffer(GL_ARRAY_BUFFER, 0);
}

//=================================================================================================
void AVbo_Bones::Delete()
{
	glDeleteBuffers(1, &Id);
}

